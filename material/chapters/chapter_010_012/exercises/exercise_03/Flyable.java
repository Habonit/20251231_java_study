package chapters.chapter_010_012.exercises.exercise_03;

/**
 * 연습 문제 3: 로봇 공장 시스템 (심화)
 *
 * ========================================
 * 학습 목표
 * ========================================
 * - 인터페이스의 다중 구현을 이해하고 활용한다
 * - 클래스 상속과 인터페이스 구현의 차이를 이해한다
 * - 다이아몬드 문제가 인터페이스에서는 발생하지 않음을 이해한다
 *
 * ========================================
 * 문제 설명
 * ========================================
 * 로봇 공장에서 다양한 기능을 가진 로봇을 생산하려고 한다.
 * 로봇은 다음과 같은 기능 조합을 가질 수 있다:
 *
 * - 비행 (Flyable)
 * - 수영 (Swimmable)
 * - 걷기 (Walkable)
 *
 * 각 로봇은 여러 기능을 동시에 가질 수 있다.
 * 예: AirRobot은 비행 + 걷기, SeaRobot은 수영 + 걷기
 *
 * ========================================
 * 요구사항
 * ========================================
 * 1. 3개의 인터페이스를 완성하세요
 *    - Flyable: fly() 메서드
 *    - Swimmable: swim() 메서드
 *    - Walkable: walk() 메서드
 *
 * 2. 다음 로봇 클래스를 구현하세요
 *    - GroundRobot: Walkable만 구현
 *      출력: "지상 로봇이 걷습니다."
 *
 *    - AirRobot: Flyable, Walkable 구현 (다중 구현)
 *      출력: "에어 로봇이 비행합니다.", "에어 로봇이 걷습니다."
 *
 *    - SeaRobot: Swimmable, Walkable 구현 (다중 구현)
 *      출력: "씨 로봇이 수영합니다.", "씨 로봇이 걷습니다."
 *
 *    - SuperRobot: Flyable, Swimmable, Walkable 모두 구현 (다중 구현)
 *      출력: "슈퍼 로봇이 비행합니다.", "슈퍼 로봇이 수영합니다.", "슈퍼 로봇이 걷습니다."
 *
 * 3. RobotMain에서 다형적 참조를 활용하여 각 기능을 테스트하세요
 *
 * ========================================
 * 실행 결과 (예상)
 * ========================================
 * === 걸을 수 있는 로봇들 ===
 * 지상 로봇이 걷습니다.
 * 에어 로봇이 걷습니다.
 * 씨 로봇이 걷습니다.
 * 슈퍼 로봇이 걷습니다.
 *
 * === 비행 가능한 로봇들 ===
 * 에어 로봇이 비행합니다.
 * 슈퍼 로봇이 비행합니다.
 *
 * === 수영 가능한 로봇들 ===
 * 씨 로봇이 수영합니다.
 * 슈퍼 로봇이 수영합니다.
 *
 * ========================================
 * 핵심 개념
 * ========================================
 * - 클래스는 다중 상속이 불가능하지만, 인터페이스는 다중 구현이 가능하다
 * - 다중 구현 시 다이아몬드 문제가 발생하지 않는 이유:
 *   인터페이스는 구현이 없는 추상 메서드만 가지므로
 *   어떤 메서드를 호출할지 모호함이 없다
 *
 * ========================================
 * 힌트
 * ========================================
 * - implements 뒤에 콤마(,)로 여러 인터페이스를 나열할 수 있다
 * - 예: public class AirRobot implements Flyable, Walkable { }
 */
