package chapters.chapter09.exercises.exercise_01;

/**
 * 동물 부모 클래스.
 *
 * 상속(Inheritance)의 핵심:
 * - 이 클래스는 Dog, Cat 등 자식 클래스에게 공통 기능을 제공한다
 * - 자식 클래스는 이 클래스의 필드와 메서드를 물려받는다
 * - 자식 클래스는 필요에 따라 메서드를 오버라이딩할 수 있다
 */
public class Animal {

    // ==========================================================
    // TODO 1: name 필드 선언 (protected)
    // - 접근 제어자: protected (같은 패키지 + 자식 클래스에서 접근 가능)
    // - 타입: String
    //
    // 힌트: protected를 사용하면 자식 클래스에서 this.name으로 접근 가능
    // ==========================================================
    protected String name;

    // ==========================================================
    // TODO 2: 생성자 구현
    // - 매개변수: String name
    // - name 필드 초기화
    // - "[name] 생성!" 출력
    // ==========================================================
    public Animal(String name) {
        this.name = name;
        System.out.println(this.name + " 생성!");
    }

    // ==========================================================
    // TODO 3: speak() 메서드 구현
    // - 반환 타입: void
    // - 출력: "동물이 소리를 냅니다."
    //
    // 이 메서드는 자식 클래스에서 오버라이딩될 예정이다
    // ==========================================================
    public void speak() {
        System.out.println(this.name + "이(가) 소리를 냅니다.");
    }

    // ==========================================================
    // TODO 4: move() 메서드 구현
    // - 반환 타입: void
    // - 출력: "[name]이(가) 이동합니다."
    //
    // 이 메서드는 자식 클래스에서 오버라이딩하지 않고 그대로 사용한다
    // ==========================================================
    public void move() {
        System.out.println(this.name + "이(가) 이동합니다.");
    }
}
