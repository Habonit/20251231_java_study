package chapters.chapter09.exercises.exercise_01;

/**
 * 고양이 클래스 - Animal을 상속받는 자식 클래스.
 *
 * 학습 포인트:
 * - extends 키워드로 Animal 클래스를 상속받는다
 * - speak() 메서드를 오버라이딩하여 "야옹~" 출력
 * - move() 메서드는 오버라이딩하지 않아 부모의 기능을 그대로 사용
 * - scratch()는 Cat만의 고유 기능
 */
public class Cat extends Animal/* TODO: Animal 상속 */ {

    // ==========================================================
    // TODO 1: Animal 클래스를 상속받도록 클래스 선언 수정
    // - extends 키워드 사용
    // ==========================================================

    // ==========================================================
    // TODO 2: 생성자 구현
    // - 매개변수: String name
    // - 부모 생성자 호출: super(name)
    // ==========================================================
    public Cat(String name) {
        super(name);
        System.out.println(this.name + " 고양이 생성!");
    }

    // ==========================================================
    // TODO 3: speak() 메서드 오버라이딩
    // - @Override 어노테이션 필수!
    // - 출력: "야옹~"
    // ==========================================================
    @Override
    public void speak() {
        System.out.println(this.name + "이(가) 야옹~ 소리를 냅니다.");
    }

    // ==========================================================
    // TODO 4: scratch() 고유 메서드 구현
    // - 반환 타입: void
    // - 출력: "[name]이(가) 긁습니다."
    // ==========================================================
    public void scratch() {
        System.out.println(this.name + "이(가) 긁습니다.");
    }
}
