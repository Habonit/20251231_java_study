/**
 * Student 클래스 - 학생 정보를 담는 사용자 정의 타입
 *
 * 클래스(Class)란?
 * - 객체를 생성하기 위한 '틀' 또는 '설계도'이다.
 * - 클래스는 객체가 가져야 할 속성(변수)과 기능(메서드)를 정의한다.
 *
 * 멤버 변수(필드)란?
 * - 클래스에 정의된 변수들을 말한다.
 * - 해당 클래스로 생성된 모든 객체가 이 변수들을 가진다.
 */
public class Student {

    /**
     * 학생 이름
     * - String 타입: 문자열을 저장
     */
    String name;

    /**
     * 학생 나이
     * - int 타입: 정수를 저장
     */
    int age;

    /**
     * 학생 성적
     * - int 타입: 정수를 저장
     */
    int grade;
}
