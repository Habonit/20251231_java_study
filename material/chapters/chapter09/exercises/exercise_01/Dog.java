package chapters.chapter09.exercises.exercise_01;

/**
 * 개 클래스 - Animal을 상속받는 자식 클래스.
 *
 * 학습 포인트:
 * - extends 키워드로 Animal 클래스를 상속받는다
 * - speak() 메서드를 오버라이딩하여 "멍멍!" 출력
 * - move() 메서드는 오버라이딩하지 않아 부모의 기능을 그대로 사용
 * - fetch()는 Dog만의 고유 기능
 */
public class Dog extends Animal/* TODO: Animal 상속 */ {

    // ==========================================================
    // TODO 1: Animal 클래스를 상속받도록 클래스 선언 수정
    // - extends 키워드 사용
    //
    // 힌트: public class Dog extends Animal
    // ==========================================================

    
    // ==========================================================
    // TODO 2: 생성자 구현
    // - 매개변수: String name
    // - 부모 생성자 호출: super(name)
    //
    // 힌트: 상속 관계에서 자식 생성자는 반드시 부모 생성자를 호출해야 함
    //       super(name)을 생성자 첫 줄에 작성
    // ==========================================================
    public Dog(String name) {
        super(name);
        System.out.println(this.name + " 개 생성!");
    }

    // ==========================================================
    // TODO 3: speak() 메서드 오버라이딩
    // - @Override 어노테이션 필수!
    // - 출력: "멍멍!"
    //
    // 질문: @Override를 안 붙이면 어떻게 될까?
    // 답: 동작은 하지만, 실수로 오타가 나면 컴파일러가 잡아주지 못함
    // ==========================================================
    @Override
    public void speak() {
        System.out.println(this.name + "이(가) 멍멍! 소리를 냅니다.");
    }

    // ==========================================================
    // TODO 4: fetch() 고유 메서드 구현
    // - 반환 타입: void
    // - 출력: "[name]이(가) 공을 물어옵니다."
    //
    // 힌트: name 필드는 부모에서 protected로 선언되어 접근 가능
    // ==========================================================
    public void fetch() {
        System.out.println(this.name + "이(가) 공을 물어옵니다.");
    }
}
