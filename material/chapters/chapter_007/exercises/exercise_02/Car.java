package chapters.chapter07.exercises.exercise_02;

/**
 * 연습 문제 2: 구매한 자동차 수
 *
 * 학습 목표:
 * - static 변수를 사용하여 인스턴스 간 공유 데이터를 관리하는 방법을 익힌다
 * - static 메서드를 구현하고 호출하는 방법을 익힌다
 *
 * 요구사항:
 * - Car 클래스를 생성할 때마다 전체 자동차 수를 카운트한다
 * - 생성 시 "차량 구입, 이름: [이름]" 을 출력한다
 * - showTotalCars() static 메서드로 구매한 차량 수를 출력한다
 *
 * 예상 출력:
 * 차량 구입, 이름: K3
 * 차량 구입, 이름: G80
 * 차량 구입, 이름: Model Y
 * 구매한 차량 수: 3
 */
public class Car {

    // ==========================================================
    // TODO 1: static 변수 선언
    // - totalCars (int): 전체 자동차 수 - private static
    //
    // 힌트: 모든 Car 인스턴스가 공유해야 하므로 static으로 선언합니다.
    // ==========================================================
    private static int totalCars;


    // ==========================================================
    // TODO 2: 인스턴스 변수 선언
    // - name (String): 자동차 이름 - private
    // ==========================================================
    private String name;

    // ==========================================================
    // TODO 3: 생성자 구현
    // - 매개변수: name (String)
    // - "차량 구입, 이름: [name]" 출력
    // - this.name에 매개변수 값 저장
    // - totalCars 1 증가
    // ==========================================================
    public Car(String name) {
        this.name = name;
        Car.totalCars++;
        System.out.println("차량 구입, 이름: " + this.name);
    }

    // ==========================================================
    // TODO 4: static 메서드 구현 - showTotalCars()
    // - 반환 타입: void
    // - "구매한 차량 수: [totalCars]" 출력
    //
    // 힌트: 이 메서드는 특정 인스턴스와 무관하게 전체 차량 수를 출력하므로
    //       static 메서드로 구현합니다.
    // ==========================================================
    public static void showTotalCars() {
        System.out.println("구매한 차량 수: " + Car.totalCars);
    }
}
